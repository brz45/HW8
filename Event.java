
public class Event {
	
	
	private int eventNumber;
	private int eventData;
	public Event(int en,int ed)
	{
		
		this.eventNumber=en;
		this.eventData=ed;
		
		
	}
	
	public int getEventData()
	{
		return eventData;
		
	}
	
	public int getEventNumber()
	{
		return eventNumber;
		
		
	}
	
	
	

}
